
package iSMS.web.objects;

import java.io.Serializable;

public class SMSChart implements Serializable {
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String week;
	
	private String month;
	
	private String year;
	
	private int nbWeekSMS;
	
	private int nbMonthSMS;
	
	private int nbYearSMS;
	
	public SMSChart() {
	}
	
	public SMSChart(String week, String month, String year, int nbWeekSMS, int nbMonthSMS, int nbYearSMS) {
		super();
		this.week = week;
		this.month = month;
		this.year = year;
		this.nbWeekSMS = nbWeekSMS;
		this.nbMonthSMS = nbMonthSMS;
		this.nbYearSMS = nbYearSMS;
	}
	
	public String getWeek() {
		
		return week;
	}
	
	public void setWeek(String week) {
		
		this.week = week;
	}
	
	public String getMonth() {
		
		return month;
	}
	
	public void setMonth(String month) {
		
		this.month = month;
	}
	
	public String getYear() {
		
		return year;
	}
	
	public void setYear(String year) {
		
		this.year = year;
	}
	
	public int getNbWeekSMS() {
		
		return nbWeekSMS;
	}
	
	public void setNbWeekSMS(int nbWeekSMS) {
		
		this.nbWeekSMS = nbWeekSMS;
	}
	
	public int getNbMonthSMS() {
		
		return nbMonthSMS;
	}
	
	public void setNbMonthSMS(int nbMonthSMS) {
		
		this.nbMonthSMS = nbMonthSMS;
	}
	
	public int getNbYearSMS() {
		
		return nbYearSMS;
	}
	
	public void setNbYearSMS(int nbYearSMS) {
		
		this.nbYearSMS = nbYearSMS;
	}
	
}
